
public class Interviewee {
	private String fName;
	private String lName;
	private String email;
	private int year; 
	private int score;
	private double GPA;
	
	public Interviewee() {
		
		
	}

	public Interviewee(String fName, String lName, String email, int year, int score, double gPA) {
		this.fName = fName;
		this.lName = lName;
		this.email = email;
		this.year = year;
		this.score = score;
		GPA = gPA;
	}
	
}

